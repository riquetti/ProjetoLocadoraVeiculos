    public void carregarListaTelefone() {
        List<SeguradoraTelefone> listaSeguradoraTelefone = dao.select();

        Object[][] dados = new Object[listaSeguradoraTelefone.size()][3];

        int i = 0;

        for (SeguradoraTelefone seguradoraTelefone : listaSeguradoraTelefone) {
            dados[i][0] = seguradoraTelefone.getId_seguradora_telefone();
            dados[i][1] = seguradoraTelefone.getNome_seguradora();
            dados[i][2] = seguradoraTelefone.getTelefone_seguradora();

            i++;
        }

        TableModel model = new DefaultTableModel(dados, new Object[]{"Id TelefoneSeguradora", "Nome Seguradora", "Telefone"}) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblSeguradoraTelefone.setModel(model);
    }

