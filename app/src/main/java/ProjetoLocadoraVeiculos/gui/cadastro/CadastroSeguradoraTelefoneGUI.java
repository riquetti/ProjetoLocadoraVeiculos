    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        int selectedRow = tblSeguradoraTelefone.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada. Selecione uma linha para editar.", "Sem Linha Selecionada", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        CadastroSeguradoraTelefoneEditarGUI dialog = new CadastroSeguradoraTelefoneEditarGUI(null, true, this);
        dialog.carregarSeguradoraTelefone((Integer) tblSeguradoraTelefone.getModel().getValueAt(selectedRow, 0));
        dialog.setVisible(true);

        carregarListaTelefone();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int id = (Integer) tblSeguradoraTelefone.getModel().getValueAt(tblSeguradoraTelefone.getSelectedRow(), 0);

        try {
            dao.delete(id);

            JOptionPane.showMessageDialog(this, "Telefone Excluido com Sucesso!", "SUCESS", JOptionPane.INFORMATION_MESSAGE);

            carregarListaTelefone();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

